// Получить сотрудников по территории
export const getEmployeesByTerritory = (territory: string): Employee[] => {
  return EMPLOYEES.filter(emp => emp.territory === territory && emp.status === 'active');
};

// Получить сотрудников по должности
export const getEmployeesByPosition = (position: 'medpred' | 'manager'): Employee[] => {
  return EMPLOYEES.filter(emp => emp.position === position && emp.status === 'active');
};

// Получить сотрудника по ID
export const getEmployeeById = (id: string): Employee | undefined => {
  return EMPLOYEES.find(emp => emp.id === id);
};

// Получить всех активных сотрудников
export const getActiveEmployees = (): Employee[] => {
  return EMPLOYEES.filter(emp => emp.status === 'active');
};

// Получить общий план сотрудника (сумма по всем препаратам)
export const getEmployeeTotalPlan = (employeeId: string, type: 'units' | 'revenue' = 'units'): number => {
  const employee = getEmployeeById(employeeId);
  if (!employee) return 0;

  const totalUnits = Object.values(employee.productPlans).reduce((sum, units) => sum + units, 0);

  if (type === 'units') {
    return totalUnits;
  } else {
    // Для расчета выручки используем среднюю цену 3500 руб
    return totalUnits * 3500;
  }
};

// Получить подчиненных сотрудника
export const getSubordinates = (managerId: string): Employee[] => {
  return EMPLOYEES.filter(emp => emp.managerId === managerId && emp.status === 'active');
};
